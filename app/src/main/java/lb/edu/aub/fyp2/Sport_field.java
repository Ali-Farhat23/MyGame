package lb.edu.aub.fyp2;

public class Sport_field {
    String sportype;
    int location_id;
    String location;
    String description;
    int manager_id;
    float price;

    public Sport_field() {

    }

    public String getSportype() {
        return sportype;
    }

    public int getLocation_id() {
        return location_id;
    }

    public String getLocation() {
        return location;
    }

    public String getDescription() {
        return description;
    }

    public int getManager_id() {
        return manager_id;
    }

    public float getPrice() {
        return price;
    }
}
